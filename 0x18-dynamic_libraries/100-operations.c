#include <stdio.h>
/**
 *add - returns the remainder
 *@a: variable a
 *@b: variable b
 *Return: add on success
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 *sub - returns the remainder
 *@a: variable a
 *@b: variable b
 *Return: sub on success
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 *mul - returns the remainder
 *@a: variable a
 *@b: variable b
 *Return: mul on success
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 *div - returns the remainder
 *@a: variable a
 *@b: variable b
 *Return: div on success
 */
int  div(int a, int b)
{
	return (a / b);
}

/**
 *mod - returns the remainder
 *@a: variable a
 *@b: variable b
 *Return: mod on success
 */

int mod(int a, int b)
{
	return (a % b);
}
